/**
 * Adapter: reads content-manifest.yaml and emits a typed TS module the app can
 * import without any runtime YAML parsing.
 *
 * Run: npm run generate:content
 *
 * The manifest is NEVER hardcoded into app code — regenerate this file whenever
 * content-manifest.yaml changes.
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { loadManifest } from "./lib/content-manifest.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const MANIFEST_PATH = resolve(
  ROOT,
  "whv-guide-content-plan/content-manifest.yaml",
);
const OUT_PATH = resolve(ROOT, "lib/content/manifest.generated.ts");

const { meta, hubs, existing, planned } = loadManifest(MANIFEST_PATH);

const j = (value) => JSON.stringify(value, null, 2);

const banner = `/**
 * AUTO-GENERATED by scripts/generate-content-registry.mjs — DO NOT EDIT.
 * Source: whv-guide-content-plan/content-manifest.yaml
 * Regenerate with: npm run generate:content
 */
import type { ContentHub, ManifestItem } from "@/types/content";
`;

const body = `
export const manifestMeta = ${j(meta)} as const;

export const manifestHubs: ContentHub[] = ${j(hubs)};

/** manifest 上で「既存」とされる記事のメタ（本文はコード側 lib/mockData.ts）。 */
export const manifestExistingItems: ManifestItem[] = ${j(existing)};

/** 計画中コンテンツ。status は planned で、公開対象ではありません。 */
export const manifestPlannedItems: ManifestItem[] = ${j(planned)};
`;

writeFileSync(OUT_PATH, banner + body, "utf8");

console.log(
  `Generated ${OUT_PATH}\n  hubs: ${hubs.length}\n  existing: ${existing.length}\n  planned: ${planned.length}`,
);
